class Attacks
{
    public string Name;
    public int DamageDone;
    string Category;
    string TypeOfDamage;

    public Attacks(string name, int damageDone, string category, string typeOfDamage)
    {
        Name = name; 
        DamageDone = damageDone; 
        Category = category;
        TypeOfDamage = typeOfDamage;
        
    }
}
